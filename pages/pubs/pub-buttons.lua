-- ── Main ──────────────────────────────────────────────────────
function Pandoc(doc)
  -- Resolve paths from metadata
  local bib_path  = ""
  local json_path = "pdf_links.json"

  if doc.meta["bibliography"] then
    bib_path = pandoc.utils.stringify(doc.meta["bibliography"])
  end
  if doc.meta["bib-file"] then
    bib_path = pandoc.utils.stringify(doc.meta["bib-file"])
  end
  if doc.meta["pdf-links"] then
    json_path = pandoc.utils.stringify(doc.meta["pdf-links"])
  end

  local pdf_links = parse_json_map(json_path)
  local bib_data  = bib_path ~= "" and parse_bib_raw(bib_path) or {}

  local n_pdf = 0
  for _ in pairs(pdf_links) do n_pdf = n_pdf + 1 end
  io.stderr:write(string.format(
    "pub-buttons.lua: %d PDF links, %d bib entries\n",
    n_pdf, (function() local c=0 for _ in pairs(bib_data) do c=c+1 end return c end)()))

  local styles_injected = false
  local new_blocks = {}

  for _, block in ipairs(doc.blocks) do
    table.insert(new_blocks, block)

    local is_refs = block.t == "Div" and (
      block.attr.identifier == "refs" or
      block.attr.identifier == "refs-books" or
      block.attr.identifier == "refs-chapters" or
      block.attr.identifier == "refs-theses")

    if is_refs then
      if not styles_injected then
        -- Insert styles before this div
        table.insert(new_blocks, #new_blocks, make_styles())
        styles_injected = true
      end

      local new_content = {}
      for _, entry in ipairs(block.content) do
        -- Pass through raw blocks (year headers etc)
        if entry.t ~= "Div" then
          table.insert(new_content, entry)
        else
          -- Check if csl-entry
          local is_entry = false
          for _, c in ipairs(entry.attr.classes) do
            if c == "csl-entry" then is_entry = true; break end
          end
          table.insert(new_content, entry)

          if is_entry then
            local id  = entry.attr.identifier        -- "ref-cheng2026"
            local key = id:gsub("^ref%-", "")        -- "cheng2026"
            local pdf = pdf_links[key]
            local bib = bib_data[key]
            local doi = bib and bib.doi or nil
            local bib_id = "bibpop-" .. key

            -- Button row
            local row = '<div class="pub-btn-row">'
            row = row .. string.format(
              '<button class="pub-btn btn-bib" onclick="showBib(\'%s\')">BibTeX</button>',
              bib_id)
            if pdf then
              row = row .. string.format(
                '<a class="pub-btn btn-pdf" href="%s" target="_blank">PDF</a>', pdf)
            end
            if doi then
              row = row .. string.format(
                '<a class="pub-btn btn-doi" href="https://doi.org/%s" target="_blank">DOI</a>',
                doi)
            end
            row = row .. '</div>'

            -- BibTeX popup
            local raw_escaped = bib and bib.raw
              :gsub("&","&amp;"):gsub("<","&lt;"):gsub(">","&gt;")
              or "(entry not found)"
            local popup = string.format(
              '<div id="%s" style="display:none">%s</div>',
              bib_id, raw_escaped)

            table.insert(new_content, pandoc.RawBlock("html", row))
            table.insert(new_content, pandoc.RawBlock("html", popup))
          end
        end
      end

      new_blocks[#new_blocks] = pandoc.Div(new_content,
        pandoc.Attr(block.attr.identifier, block.attr.classes, block.attr.attributes))
    end
  end

  doc.blocks = new_blocks
  return doc
end