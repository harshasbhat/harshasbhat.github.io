--[[
  pub-buttons.lua
  Injects BibTeX / PDF / DOI buttons below each .csl-entry.

  Requires pdf_links.json (generated by extract_pdfs.py) in the same
  directory, OR pass --metadata pdf-links="path/to/pdf_links.json".

  At build time the Makefile runs:
    python3 extract_pdfs.py MasterBibliography.bib pdf_links.json
  before pandoc.
]]

-- ── Minimal JSON string-map parser ───────────────────────────
local function parse_json_map(path)
  local map = {}
  local f = io.open(path, "r")
  if not f then
    io.stderr:write("pub-buttons.lua: cannot open " .. tostring(path) .. "\n")
    return map
  end
  local txt = f:read("*a")
  f:close()
  for k, v in txt:gmatch('"([^"]+)"%s*:%s*"([^"]+)"') do
    map[k] = v
  end
  return map
end

-- ── Parse raw BibTeX entries from .bib ───────────────────────
local function parse_bib_raw(path)
  local entries = {}
  local f = io.open(path, "r")
  if not f then return entries end
  local txt = f:read("*a")
  f:close()

  local pos = 1
  while true do
    local s, e, etype, key = txt:find("@(%w+)%s*{%s*([^,%s]+)%s*,", pos)
    if not s then break end
    if not etype:lower():match("^(string|preamble|comment)$") then
      local depth = 1
      local i = e + 1
      while i <= #txt and depth > 0 do
        local c = txt:sub(i,i)
        if     c == "{" then depth = depth + 1
        elseif c == "}" then depth = depth - 1 end
        i = i + 1
      end
      -- Strip noisy fields for clean BibTeX display
      local raw = txt:sub(s, i - 1)
      raw = raw:gsub("%s*bdsk%-file%-%d+%s*=%s*%b{}%s*,?%s*\n?", "\n")
      raw = raw:gsub("%s*bdsk%-url%-%d+%s*=%s*%b{}%s*,?%s*\n?", "\n")
      raw = raw:gsub("%s*date%-added%s*=%s*%b{}%s*,?%s*\n?", "\n")
      raw = raw:gsub("%s*date%-modified%s*=%s*%b{}%s*,?%s*\n?", "\n")
      raw = raw:gsub("\n\n+", "\n")
      -- Extract doi
      local doi = raw:match("[Dd][Oo][Ii]%s*=%s*[{\"](.-)[}\"]")
      doi = doi and doi:gsub("%s+","") or nil
      entries[key] = { raw = raw, doi = doi }
    end
    pos = e + 1
  end
  return entries
end

